export default class Math {
    sum(a:number, b:number): number {
        return a + b
    }
    substract(a:number, b:number): number {
        return a - b
    }
    multiply(a:number, b:number): number {
        return a * b
    }
    divison(a:number, b:number): number {
        return a / b
    }
}